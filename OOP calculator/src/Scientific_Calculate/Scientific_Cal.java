    private void jbtnconverterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnconverterActionPerformed
        if ((jtxtEnter1.getText().equals(""))||(jtxtEnter2.getText().equals("")))
        {
            JOptionPane.showMessageDialog(null,"You must enter value to compute",
                    "DST System", JOptionPane.INFORMATION_MESSAGE);
        }
        
        else if(jCDistance.getSelectedItem().equals("Distance"))
        {
            double dst1 = Double.parseDouble(jtxtEnter1.getText());
            double dst2 = Double.parseDouble(jtxtEnter2.getText());
            double dst3;
            //distance Formula = speed * time
            j1b1Display1.setText("Distance = ");
            j1b1Display2.setText("Speed ");
            jtxtX.setText("X");
            j1b1Display3.setText("Time");
            dst3 = dst1 * dst2;
            String value = String.format("%.2f", dst3);
            j1b1Result.setText(value);
        }
        else if(jCDistance.getSelectedItem().equals("Speed"))
        {
            double dst1 = Double.parseDouble(jtxtEnter1.getText());
            double dst2 = Double.parseDouble(jtxtEnter2.getText());
            double dst3;
            //Speed Formula = Distance / time
            j1b1Display1.setText("Speed = ");
            j1b1Display2.setText("Distance ");
            jtxtX.setText("/");
            j1b1Display3.setText("Time");
            dst3 = dst1 / dst2;
            String value = String.format("%.2f", dst3);
            j1b1Result.setText(value);
        }
        else if(jCDistance.getSelectedItem().equals("Time"))
        {
            double dst1 = Double.parseDouble(jtxtEnter1.getText());
            double dst2 = Double.parseDouble(jtxtEnter2.getText());
            double dst3;
            //Time Formula = Distance / Speed
            j1b1Display1.setText("Time = ");
            j1b1Display2.setText("Distance ");
            jtxtX.setText("/");
            j1b1Display3.setText("Speed");
            dst3 = (dst1 * dst2) / dst2;
            String value = String.format("%.2f", dst3);
            j1b1Result.setText(value);
        }
        else if(jCDistance.getSelectedItem().equals("Work"))
        {
            double dst1 = Double.parseDouble(jtxtEnter1.getText());
            double dst2 = Double.parseDouble(jtxtEnter2.getText());
            double dst3;
            //Work Formula = Force * Displacement
            j1b1Display1.setText("Work = ");
            j1b1Display2.setText("Force ");
            jtxtX.setText("X");
            j1b1Display3.setText("Displacement");
            dst3 = dst1 * dst2;
            String value = String.format("%.2f", dst3);
            j1b1Result.setText(value);
        }
        else if(jCDistance.getSelectedItem().equals("Acceleration"))
        {
            double dst1 = Double.parseDouble(jtxtEnter1.getText());
            double dst2 = Double.parseDouble(jtxtEnter2.getText());
            double dst3;
            //Acceleration Formula = velocity / Time
            j1b1Display1.setText("Acceleration = ");
            j1b1Display2.setText("velocity ");
            jtxtX.setText("/");
            j1b1Display3.setText("Time");
            dst3 = dst1 / dst2;
            String value = String.format("%.2f", dst3);
            j1b1Result.setText(value);
        }
        else if(jCDistance.getSelectedItem().equals("Force"))
        {
            double dst1 = Double.parseDouble(jtxtEnter1.getText());
            double dst2 = Double.parseDouble(jtxtEnter2.getText());
            double dst3;
            //Force Formula = Mass * Acceleration
            j1b1Display1.setText("Force = ");
            j1b1Display2.setText("Mass ");
            jtxtX.setText("X");
            j1b1Display3.setText("Acceleration");
            dst3 = dst1 * dst2;
            String value = String.format("%.2f", dst3);
            j1b1Result.setText(value);
        }
        else if(jCDistance.getSelectedItem().equals("Power"))
        {
            double dst1 = Double.parseDouble(jtxtEnter1.getText());
            double dst2 = Double.parseDouble(jtxtEnter2.getText());
            double dst3;
            //Power Formula = Work * Time
            j1b1Display1.setText("Power = ");
            j1b1Display2.setText("Work ");
            jtxtX.setText("/");
            j1b1Display3.setText("Time");
            dst3 = dst1 / dst2;
            String value = String.format("%.2f", dst3);
            j1b1Result.setText(value);
        }
        else if(jCDistance.getSelectedItem().equals("Momentum"))
        {
            double dst1 = Double.parseDouble(jtxtEnter1.getText());
            double dst2 = Double.parseDouble(jtxtEnter2.getText());
            double dst3;
            //Momentum Formula = Mass * Speed
            j1b1Display1.setText("Momentum = ");
            j1b1Display2.setText("Mass ");
            jtxtX.setText("X");
            j1b1Display3.setText("Speed");
            dst3 = dst1 * dst2;
            String value = String.format("%.2f", dst3);
            j1b1Result.setText(value);
        }
    }//GEN-LAST:event_jbtnconverterActionPerformed

