    private void jbtnisequalsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnisequalsActionPerformed
        String answer;
        secondnum = Double.parseDouble(jtxtDisplay.getText());
        if (operations == "+")
        {
         result = firstnum + secondnum;
         answer = String.format("%.0f",result);
         jtxtDisplay.setText(answer);
        }
        else if (operations == "-")
        {
         result = firstnum - secondnum;
         answer = String.format("%.0f",result);
         jtxtDisplay.setText(answer);
        }
        else if (operations == "*")
        {
         result = firstnum * secondnum;
         answer = String.format("%.0f",result);
         jtxtDisplay.setText(answer);
        }
        else if (operations == "/")
        {
         result = firstnum / secondnum;
         answer = String.format("%.0f",result);
         jtxtDisplay.setText(answer);
        }
        else if (operations == "%")
        {
         result = firstnum % secondnum;
         answer = String.format("%.0f",result);
         jtxtDisplay.setText(answer);
        }
    }//GEN-LAST:event_jbtnisequalsActionPerformed

